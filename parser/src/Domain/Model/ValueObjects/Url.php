namespace Root\Parser\Domain\Model\ValueObjects;

class Url
{
    /**
     * @var string
     */
    private string $url;

    /**
     * @return string
     */
    public function getUrl(): string
    {
        return $this->url;
    }

    /**
     * @param string $url
     */
    public function __construct(string $url)
    {
        $this->url = $url;
    }
}